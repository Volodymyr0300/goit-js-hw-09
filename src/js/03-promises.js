form.addEventListener('submit', event => {
  event.preventDefault();
  const delay = Number(form.elements.delay.value);
  //   console.log('ğŸš€ ~ file: 03-promises.js:28 ~ delay:', delay);
  const step = Number(form.elements.step.value);
  //   console.log('ğŸš€ ~ file: 03-promises.js:30 ~ step:', step);
  const amount = Number(form.elements.amount.value);
  //   console.log('ğŸš€ ~ file: 03-promises.js:32 ~ amount:', amount);

  let position = 0;

  for (let i = 1; i <= amount; i += 1) {
    position += i;

    const promiseDelay = delay + (i - 1) * step;

    createPromise(position, promiseDelay)
      .then(({ position, delay }) => {
        console.log(`âœ… Fulfilled promise ${position} in ${delay}ms`);
      })
      .catch(({ position, delay }) => {
        console.log(`âŒ Rejected promise ${position} in ${delay}ms`);
      });
  }
});
